    public void loadHasil(String where) {
        try {
            Object[][] data = null;
            Object[] header = {"NO", "NAMA SISWA", "NISN", "SKOR", "STATUS"};
            DefaultTableModel model = new DefaultTableModel(data, header);
            hasilPembobotan.setModel(model);
            int nomor = 0;

            //load data from DB
            Connection koneksi = Koneksi.konekKeDB();
            Statement st = koneksi.createStatement();
            String query = "SELECT * FROM hasil" + where+ "Order By skor DESC";
            ResultSet rs = st.executeQuery(query);
            while (rs.next()) {
                int id = rs.getInt("id");
                String nama = rs.getString("nama");
                String nisn = rs.getString("nisn");
                String skor = rs.getString("skor");
                String status = rs.getString("status");
                nomor++;

                Object[] d = {nomor, nama, nisn, skor, status};
                model.addRow(d);
            }
        } catch (Exception e) {

        }
    }

