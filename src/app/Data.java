public class Data {

    private String id;
    private String nama;
    private String nisn;
    private String indo;
    private String mtk;
    private String ingg;
    private String ipa;
    private String jarak;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getNisn() {
        return nisn;
    }

    public void setNisn(String nisn) {
        this.nisn = nisn;
    }

    public String getIndo() {
        return indo;
    }

    public void setIndo(String indo) {
        this.indo = indo;
    }

    public String getMtk() {
        return mtk;
    }

    public void setMtk(String mtk) {
        this.mtk = mtk;
    }

    public String getIngg() {
        return ingg;
    }

    public void setIngg(String ingg) {
        this.ingg = ingg;
    }

    public String getIpa() {
        return ipa;
    }

    public void setIpa(String ipa) {
        this.ipa = ipa;
    }

    public String getJarak() {
        return jarak;
    }

    public void setJarak(String jarak) {
        this.jarak = jarak;
    }

}
